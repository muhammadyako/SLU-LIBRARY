                  <p class="mb-15">The Haruna Wakili Library is committed to creating an inclusive and accessible environment for all its users, including those with special needs. By providing tailored services, adaptive technologies, and a supportive atmosphere, the library ensures that everyone can access its resources and participate fully in academic and research activities. This note outlines the various initiatives and support mechanisms in place for people with special needs at the Haruna Wakili Library.</p>
				  <h4>Infrastructure and Physical Accessibility</h4>
				  <li><b>Accessible Entrances and Exits: </b></li>
                 
				  <p>The library is designed with accessible entrances and exits, featuring ramps and automatic doors to facilitate easy access for users with mobility impairments. Clear signage guides users to these accessible points.</p>
                  <li><b>Accessible Restrooms</b></li>
				  
				  
				 <p>Accessible restrooms are available on each floor, designed to accommodate users with disabilities. These restrooms provide ample space, and other necessary adaptations.</p>
				
				<li><b>Designated Seating Areas:</b></li>
				<p>The library provides designated seating areas for people with special needs. These areas are equipped with adjustable tables and chairs to accommodate various physical requirements.</p>
				
				<h4>Assistive Technologies</h4>
				<li><b>Screen Readers and Magnifiers:</b></li>
				
				<p>Computers in the library are equipped with screen readers and magnification software to assist users with visual impairments. These tools help in accessing digital content, reading e-books, and navigating online resources.</p>
				<li><b>Hearing Assistance Devices: </b></li>
				<p>For users with hearing impairments, the library provides hearing assistance devices, including induction loop systems and personal amplifiers. These devices help in better communication and participation in library activities.</p>
				<li><b>Text-to-Speech Software: </b></li>
				
				<p>Text-to-speech software is installed on library computers to assist users with reading difficulties or visual impairments. This software converts written text into spoken words, facilitating easier access to information.</p>
				
				<h4>Specialized Services</h4>
				
				<li><b>Personalized Assistance:</b></li>
				<p>Library staff are trained to provide personalized assistance to users with special needs. This includes help with locating materials, using assistive technologies, and accessing digital resources.</p>
				<li><b>Accessible Formats:</b></li>
				<p>The library offers materials in accessible formats, such as braille books, large print books, and audiobooks. Users can request specific formats based on their needs, ensuring equal access to information.</p>
				
				<li><b>Interlibrary Loan Services: </b></li>
				
				<p>For materials not available in accessible formats, the library’s interlibrary loan service can obtain resources from other institutions. This service expands the range of accessible materials available to users with special needs.</p>
				
				
				<li><b>Extended Loan Periods: </b></li>
				<p>Recognizing the additional time that may be needed to use materials, the library offers extended loan periods for users with special needs. This accommodation helps ensure that all users can fully benefit from the library’s resources.</p>
				
				<h4>Programs and Workshops</h4>
				<li><b>Information Literacy Workshops:</b></li>
				<p>The library conducts information literacy workshops tailored for users with special needs. These workshops cover topics such as using assistive technologies, navigating the library’s resources, and effective research strategies.</p>
				
				<li><b>Support Groups and Networking:</b></li>
				<p>The library facilitates support groups and networking opportunities for users with special needs. These groups provide a platform for sharing experiences, resources, and strategies for academic success.</p>
				
