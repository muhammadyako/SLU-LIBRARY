                  <p class="mb-15">Haruna Wakili Library is dedicated to providing comprehensive services that support the academic and research needs of its community. Among its valuable offerings is the laptop lending service, which plays a crucial role in bridging the digital divide and enhancing access to technology for students, faculty, and researchers. This note outlines the scope, importance, and procedures associated with the laptop lending service at Haruna Wakili Library.</p>
				  <h4>Scope of Laptop Lending Services</h4>
				  <li><b>Availability: </b></li>
                 
				  <p>The library maintains a fleet of laptops available for lending to registered users. These laptops are equipped with necessary software and applications required for academic work, including word processors, spreadsheets, presentation tools, and specialized academic software.</p>
                  <li><b>Short-Term and Long-Term Loans:</b></li>
				  
				  <b>-	Short-Term Loans:</b>
				        <p>Laptops can be borrowed for a few hours or a day, catering to users who need temporary access to a computer for tasks such as class assignments, presentations, or research.</p>
				
				 <b>-Long-Term Loans: </b>
				        <p>Extended borrowing periods are available for users who need a laptop for a longer duration, such as an entire semester. This option is particularly beneficial for students without personal computers.</p>
				<h4>Importance of Laptop Lending Services</h4>
				<b>-	Bridging the Digital Divide: </b>
				
				<p>Laptop lending services help to bridge the digital divide by providing access to technology for those who may not own personal devices. This is essential for ensuring that all students and researchers can participate fully in academic activities.</p>
				<b>-	Supporting Academic Success: </b>
				<p>Access to laptops allows students to complete assignments, conduct research, and participate in online classes and collaborative projects. This support is crucial for academic success, particularly in a digital-first learning environment.</p>
				<b>-	Flexibility and Convenience: </b>
				
				<p>The service offers flexibility and convenience, enabling users to work from different locations within or outside the campus. This adaptability is particularly valuable for users with varying schedules and commitments.</p>
				
				<h4>Procedures for Laptop Lending</h4>
				
				<li><b>Eligibility and Registration: </b></li>
				<p>To be eligible for the laptop lending service, users must be registered members of the library. They need to complete a registration process that includes providing valid identification and agreeing to the library’s terms and conditions for laptop use.</p>
				<li><b>Reservation and Checkout: </b></li>
				<p>Laptops can be reserved online through the library’s portal or in person at the lending desk. The checkout process includes signing a lending agreement that outlines the user’s responsibilities, such as proper care of the laptop and adherence to the return date.</p>
				<li><b>Usage Guidelines: </b></li>
				<p>Users are provided with guidelines on the proper use of the laptops. This includes instructions on how to handle the devices, use the installed software, and what to do in case of technical issues or damages</p>
				<li><b>Return and Inspection:</b></li>
				<p>Upon return, each laptop undergoes a thorough inspection to ensure it is in good working condition. Any issues or damages are documented, and users may be held accountable for repairs or replacements if the damage is due to misuse.</p>
				<li><b>Renewal and Extension: </b></li>
				<p>Users can request renewals or extensions of their laptop loans, subject to availability and demand. The library’s system allows for easy tracking and management of loan periods to ensure fair access for all users.</p>
				<h4>User Support Services</h4>
				<li><b>Technical Assistance:</b></li>
				<p>The library provides technical support to users experiencing issues with borrowed laptops. This includes troubleshooting hardware and software problems, as well as providing guidance on using specific applications.</p>
				<li><b>Orientation and Training:</b></li>
				<p>Orientation sessions are available to help users become familiar with the laptops and the installed software. These sessions ensure that users can effectively utilize the devices for their academic needs.</p>
				<li><b>Help Desk: </b></li>
				<p>A dedicated help desk is available for users to seek assistance with laptop lending services. This includes help with reservations, renewals, and any issues that arise during the loan period.</p>
